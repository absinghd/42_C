char*	ft_strcat(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	char *newS = s1;
	if ((newS != '\0') && (s2 != '\0'))
	{
		while (newS[i] != '\0')
		{
			i++;
		}
		while (s2[j] != '\0')
		{
			newS[i] += s2[j] ;
			i++;
			j++;
		}
	}
}
