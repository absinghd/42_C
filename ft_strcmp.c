char ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int diff;

	i = 0;

	if((s1 != '\0') && (s2 != '\0'))
	{
		if (s1 == s2)
		{
			diff = 0;
		}
		else
		{
			while (s1[i] == s2[i])
			{
				i++;
			}
			diff = (s1[i] - s2[i]);
		}
	}
	return(diff);
}
