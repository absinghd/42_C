char* ft_strncat(char *s1, char *s2, int num)
{
	int i;
	int j;

	j = 0;
	i = 0;
	char *newS = s1;
	if ((newS != '\0') && (s2 != '\0'))
	{
		while (newS[i] != '\0')
		{
			i++;
		}
		while ((j <= num) && (s2[j] != '\0')) 
		{
			newS[i] += s2[j];
			j++;
			num++;
		}		
	}
	return (newS);
}

